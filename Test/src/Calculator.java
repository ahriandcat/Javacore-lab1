public class Calculator {
    int a;
    int b;
    public int tinh_tong () {
        return a+b;
    }
    public int tinh_hieu () {
        return a-b;
    }
    public int tinh_tich () {
        return a*b;
    }
    public double tinh_thuong () {
        return (double)a/(double)b;
    }
}
